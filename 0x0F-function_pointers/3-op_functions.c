#include "3-calc.h"
#include <stdlib.h>


/**
 * op_add - sum of two numbers
 * @a: first number
 * @b: second number
 *
 * Return: result of number
 */
int op_add(int a, int b)
{
	return (a + b);
}


/**
 * op_mul -  two numbers
 * @a: first number
 * @b: second number
 *
 * Return: result of number
 */
int op_mul(int a, int b)
{
	return (a * b);
}


/**
 * op_sub - two numbers
 * @a: first number
 * @b: second number
 *
 * Return: result of number
 */
int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_mod - two numbers
 * @a: first number
 * @b: second number
 *
 * Return: result of number
 */
int op_mod(int a, int b)
{
	return (a % b);
}


/**
 * op_div - two numbers
 * @a: first number
 * @b: second number
 *
 * Return: result of number
 */
int op_div(int a, int b)
{
	return (a / b);
}
